def get_scenario_prompt(mode_id: str, scenario_id: str | None) -> str:
    """Системный промпт сценария; неизвестный/пустой — фолбэк на дефолт режима."""
    mode = get_mode(mode_id)
    sid = scenario_id or mode.default_scenario_id
    by_mode = SCENARIOS.get(mode_id, {})
    scenario = by_mode.get(sid) or by_mode.get(mode.default_scenario_id)
    return scenario.system_prompt if scenario else ""
